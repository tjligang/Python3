#!/usr/bin/env python3
# -*- coding: utf-8 -*-

print('hello, world')
# 逗号分隔多个字符串，依次打印每个字符串，遇到逗号会输出一个空格
print('The quick brown fox', 'jumps over', 'the lazy dog')

# 打印整数，或者计算结果
print(300)
print(100 + 200)
print('100 + 200 =', 100 + 200)

# 用户输入字符串，并存放到一个变量里
name = input('please enter your name: ')
print('hello,', name)